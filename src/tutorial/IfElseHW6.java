public class IfElseHW6 {
    public static void main(String[] args) {
        int a = 80;
        if (a == 20) {
            System.out.println("false");
        }

        if (a > 20) {
            System.out.println(a * 2);
        } else if (a < 20) {
            System.out.println(a / 2);
        }
    }
}
